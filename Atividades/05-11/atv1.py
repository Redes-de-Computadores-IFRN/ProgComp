#   TEMPERATURA
'''
Faça um programa que o usuários informe uma temperatura em Celsius e exiba essa mesma temperatura em Fahrenheit.
'''

c = float(input('Insira a temperatura em Celsius: '))
f = (c * (9 / 5)) + 32

print('__________________________________________')
print(
    f' Celsius: {c}° \n Fahrenheit: {f}°'
)
print('__________________________________________')